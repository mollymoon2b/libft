#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	i;
	int		j;

	j = 0;
	i = c;
	while (s[j] != i)
	{
		if (!s[j])
			return (NULL);
		j++;
	}
	return ((char *)(s + j));
}
