#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*i;
	char	*j;

	i = s1;
	j = (char *)s2;
	while (*i != '\0')
		i++;
	while (*j != '\0')
	{
		*i = *j;
		i++;
		j++;
	}
	*i = '\0';
	return (s1);
}
